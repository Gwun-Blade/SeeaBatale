package my.sb.game;

/**
 * int pair
 * */

public class Pair {
    private int ferst;
    private int second;

    public Pair(int ferst, int second) {
        this.ferst = ferst;
        this.second = second;
    }

    public int getFerst() {
        return ferst;
    }

    public void setFerst(int ferst) {
        this.ferst = ferst;
    }

    public int getSecond() {
        return second;
    }

    public void setSecond(int second) {
        this.second = second;
    }
}
